#string Uppercase to Lowercase in Python

print("In this python script we will learn conversion of String from Uppercase to Lowercase and vice-versa\n\n")

msg = "hi i am anushka"
print("String before conversion '",msg,"'")
print("This is conversion of string from lowercase to uppercase '",msg.upper(),"'")

msg_2 = "HI I AM ANUSHKA"
print("String before conversion '",msg_2,"'")
print("This is conversion of string from uppercase to lowercase '",msg.lower(),"'")

